#include "ft.h"

int add(int nbr1, int nbr2)
{
    return (nbr1 + nbr2);
}

int sub(int nbr1, int nbr2)
{
    return (nbr1 - nbr2);
}

int div(int nbr1, int nbr2)
{
    return (nbr1 / nbr2);
}

int mul(int nbr1, int nbr2)
{
    return (nbr1 * nbr2);
}

int mod(int nbr1, int nbr2)
{
    return (nbr1 % nbr2);
}
